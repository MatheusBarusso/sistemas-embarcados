// --- HELPER FUNCTION ---
// We use this so we don't have to repeat screen commands in the loop
void showOnScreen(String message) {
  display.clearDisplay();              // Clear the old text
  display.setTextSize(2);              // Size 2 makes it nice and big
  display.setTextColor(SSD1306_WHITE); // White text
  
  // Set cursor slightly down from the top to center it (x=0, y=8)
  display.setCursor(0, 8);             
  display.println(message);
  
  display.display();                   // Send the buffer to the screen
  
  // Keep sending to Serial Monitor just in case you need to debug
  Serial.println(message);             
}

void showBitmap(const unsigned char* bitmap, int width, int height) {
  display.clearDisplay();
  display.drawBitmap(0, 0, bitmap, width, height, SSD1306_WHITE);
  display.display();
}

void select_image() {
  const unsigned char* imagens[] = {NES, GENESIS, GNC};
  const unsigned char* imagens_sel[] = {NES_selected, GENESIS_selected, GNC_selected};

  if (is_selected) {
    showBitmap(imagens_sel[current_item], 128, 64);
  } else {
    showBitmap(imagens[current_item], 128, 64);
  }
}

void setup() {
  Serial.begin(115200);
  delay(1000);
  
  // 1. Start the Joystick and controllers
  controller.begin();
  genesis.begin();
  nes.begin();
  USB.begin();
  gamepad.begin();

  // 2. Start the I2C bus with your specific pins
  Wire.begin(SDA_PIN, SCL_PIN);

  // 3. Start the OLED Display
  if(!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) {
    Serial.println(F("SSD1306 failed! Check wiring."));
    for(;;); // Freeze here if the screen doesn't connect
  }

  // Show an initial message
  showBitmap(selection_screen, 128, 64);
}

void loop() {
  controller.update();

  // Navegation
  if (!is_selected) {
    if (controller.wentUp()) {
      current_item = (current_item + 1) % 3;
      select_image();
    }

    if (controller.wentDown()) {
      current_item = (current_item - 1 + 3) % 3;
      select_image();
    }
  }
  if (controller.clicked()) {
    is_selected = !is_selected;
    select_image();
  }

  if (is_selected) {
    ControllerState s = {0, 0};

    // current_item 0 = NES, current_item 1 = GENESIS, baseando-se no seu array de imagens
    if (current_item == 0) { 
      s = nes.read();
    } 
    else if (current_item == 1) {
      s = genesis.read();
    }

    gamepad.send(
      0, 0, 0,   // x, y, z
      0, 0, 0,   // rz, rx, ry
      s.hat,
      s.buttons
    );

    delay(10);
  }
}